ALIMENTACIÓN ILIMITADA<br/><br/>

En nuestros Hoteles podrás disfrutar exquisitos y variados desayunos y almuerzos al estilo buffet. Al llegar la noche podrás compartir con los tuyos de cenas a la carta, con variadas opciones.
Y para darle el punto final a tu comida podrás deleitarte con fabulosos postres y frutas exóticas, sólo tienes que reservar en el restaurante de tu preferencia. Finalmente, si deseas algo rápido, todos nuestros hoteles ofrecen deliciosos snacks al mediodía y a la medianoche.
