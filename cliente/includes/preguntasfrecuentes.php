<h4> 1. Tenemos niños pequeños, ¿podemos pedir camas supletorias o cunas?</h4>
La información sobre niños y camas supletorias/cunas se encuentra en el apartado de condiciones del hotel. Atención:<br/>
- Los suplementos de los niños no están incluidos en el precio de la reserva.<br/>
- Solicita las camas supletorias/cunas durante el proceso de reserva.<br/>
- Si ya has reservado, podrás añadir camas supletorias haciendo clic en el enlace que aparece en el e-mail de confirmación.<br/>
- Es recomendable que te pongas en contacto con el hotel antes de llegar para garantizar tu petición. Encontrarás los datos de contacto del hotel en el e-mail de confirmación.<br/>
 <br/><br/>
2. ¿Qué diferencia hay entre una habitación doble y una habitación doble - 2 camas?<br/>
Una habitación doble tiene una cama doble, mientras que una habitación doble - 2 camas cuenta con 2 individuales. Si se trata de una habitación doble - 1 o 2 camas, puede referirse a cualquiera de las dos opciones anteriores. Podrás indicar el tipo de cama que prefieres en el apartado de peticiones especiales, durante el proceso de reserva.  
<br/><br/>
 3. ¿Qué significa "no reembolsable" o "cancelación gratuita"?<br/>
Cada habitación tiene unas condiciones distintas (establecidas por el hotel).
- Si una habitación es no reembolsable significa que si cancelas o modificas la reserva, se aplicarán cargos. El importe que habrá que abonar está indicado en las condiciones de la habitación y en la confirmación de la reserva.
- Si una habitación es con cancelación gratuita significa que puedes cambiar o modificar la reserva de forma gratuita, si se hace dentro del plazo establecido por el hotel. El plazo está indicado en las condiciones de la habitación y en la confirmación de la reserva (por ejemplo, "puedes cancelar hasta x días" o "puedes cancelar antes del dd/mm/aa hh:mm".
<br/><br/>
4. ¿Qué incluye el precio?<br/>
Al lado del tipo de habitación verás un listado con todos los servicios incluidos en el precio. Para verlo, haz clic en el nombre de la habitación. Si pasas el cursor por el texto de la columna Condiciones, podrás saber si el desayuno o los impuestos están incluidos en el precio 
<br/><br/>
5. ¿El precio incluye impuestos?<br/>
Depende del hotel y del tipo de habitación. Para saberlo, consulta el texto que hay debajo del tipo de habitación
<br/><br/>
6. ¿El precio incluye desayuno?<br/>
Depende del hotel y del tipo de habitación. Para saberlo, consulta el texto que hay debajo del tipo de habitación.
<br/><br/>
7. Para reservar tengo que introducir los datos de mi tarjeta, pero ¿cuándo pago?<br/>
El procedimiento de pago de tu estancia depende de cada hotel y del tipo de habitación elegido. Puedes consultar cuándo se te cargará la reserva en el apartado de "Condiciones del hotel", en el apartado de "Condiciones" de la descripción de la habitación y en la confirmación de tu reserva.
<br/><br/>
8. ¿Los hoteles solicitan depósitos o pagos por adelantado?<br/>
La mayoría de hoteles no exige el pago por adelantado o de un depósito. No obstante, encontrarás esta información en el apartado de condiciones.
<br/><br/>
9. Llegaré antes/después de la hora de entrada establecida por el alojamiento. ¿Podré hacer el registro de entrada de todas formas?<br/>
Puedes solicitar un registro de entrada más pronto o más tarde de lo habitual de muchas formas:
- Puedes indicar tu hora prevista de llegada durante el proceso de reserva
- Puedes gestionar tu reserva online y solicitar un registro de entrada fuera del horario habitual.
- Puedes ponerte en contacto con el alojamiento directamente con los datos de contacto que aparecen en la confirmación de tu reserva.
<br/><br/>
10. ¿Cuál es el horario de registro de entrada (check-in) y el de salida (check-out)?<br/>
Las encontrarás en el apartado de condiciones del hotel.
<br/><br/>
11. ¿Dónde puedo encontrar más información sobre las instalaciones del hotel o de la habitación?<br/>
<br/><br/>
Las instalaciones de la habitación aparecen en el apartado de tipo de habitación. Para ver fotos y un listado completo, haz clic en el tipo de habitación que te interese. Las instalaciones del hotel aparecen más abajo, en el apartado de servicios. 
<br/><br/>
12. Quiero hacer el registro de salida después de la hora prevista. ¿Qué tengo que hacer?<br/>
Es necesario que lo consultes con el hotel y, normalmente, depende de la disponibilidad durante tu estancia. Puedes consultarlo en recepción a la llegada. 
<br/><br/>
13. Quiero una habitación de fumadores pero solo puedo elegir de no fumadores. ¿Cómo puedo solicitarla?<br/>
Si no aparecen habitaciones para fumadores, significa que el hotel no permite fumar en la habitaciones. 
<br/><br/>
14. ¿Cómo sé si el hotel admite mascotas?<br/>
Las condiciones referentes a mascotas se encuentran en el apartado de condiciones del hotel. 
